            <h2>Sushi Knife</h2>
            <img src="images/sushi.jpg" width="230">
            <p> This long but thin blade is an absolute necessity when preparing most Oriental
            dishes, most notably sushi. The knife's spine is soft and flexible, which concentrates
            force on the durable outer layers of the blade. This aids in cutting rice rolls into 
            perfectly uniform shapes and sizes without much resistance.</p>
            
            <h2>Cleaver</h2>
            <img src="images/cleaver.jpg" width="230">
            <p>	This heavy blade is a must-have for working with large portions of protein. Cutting
